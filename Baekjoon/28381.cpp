#include <bits/stdc++.h>
using namespace std;

#define nl '\n'

using ll = long long;

int main() {
    ios_base::sync_with_stdio(0);
    cin.tie(0); cout.tie(0);

    int d;
    cin >> d;

    if (d <= 9) {
      cout << "30 50 99" << nl;
    } else {
      cout << "1 2 3" << nl;
    }

    return 0;
}
